class Evenoutput
{
 public static void main(String args[])
 {
  int start=1,end=10;
  while(start<=end)
  {
    
    if(start%2==0)
     
     System.out.println(start);
    
   start++;
  }
  int nstart=1,nend=10;
  while(nstart<=nend)
  {
    
    if(nstart%2!=0)
    
     System.out.println(nstart);
    
   nstart++;
  }
 }
}