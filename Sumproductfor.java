class Sumfor
{
 public static void main(String ars[])
 {
  int start,end,sum=0;
  for(start=1,end=10;start<=end;start++)
   {
    sum+=start;
   
     System.out.println(sum);
  
  }
}
}


class Productfor
{
 public static void main(String ars[])
 {
  int start,end,product=1;
  for(start=1,end=10;start<=end;start++)
   {
    product*=start;
     System.out.println(product);
  
  }
}
}